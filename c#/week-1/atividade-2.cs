using System;

public class Atividade2
{
    public static void Main(string[] args)
    {
        int small = 10;
        int medium = 12;
        int large = 15;
        
        Console.WriteLine ("Gerenciador de Vendas de Roupas");
        Console.WriteLine("\tCamiseta pequena: 10 reais");
        Console.WriteLine("\tCamiseta media: 12 reais");
        Console.WriteLine("\tCamiseta grande: 15 reais\n");

       Console.WriteLine("Quantas camisetas pequenas?");
       int qntsmall = Convert.ToInt32(Console.ReadLine());
       small = qntsmall*small;
       
       Console.WriteLine("Quantas camisetas medias?");
       int qntmedium = Convert.ToInt32(Console.ReadLine());
       medium = qntmedium*medium;
       
       Console.WriteLine("Quantas camisetas grandes?");
       int qntlarge = Convert.ToInt32(Console.ReadLine());
       large = qntlarge*large;
       
       int total = small + medium + large;
       
       Console.WriteLine($"\nO valor total da venda foi de R${total}");
       
    }
}
