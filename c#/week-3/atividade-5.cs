using System;


public class HelloWorld
{
    public static void Main(string[] args)
    {
        Console.WriteLine ("Digite um numero inteiro");
        double num = double.Parse(Console.ReadLine());
        
        if (num >= 0) {
            num = Math.Sqrt(num);
            Console.WriteLine($"{num}");
        } else {
            num = num * num;
            Console.WriteLine($"{num}");
        }
    }
}
